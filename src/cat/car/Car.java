package cat.car;

public class Car {
    private String brand, color;
    private float speed;

    public String getBrand() {
        return this.brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public float getSpeed(){
        return this.speed;
    }

    public void setSpeed(float speed) {
        this.speed = speed;
    }

    public String getColor(){
        return this.color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}

